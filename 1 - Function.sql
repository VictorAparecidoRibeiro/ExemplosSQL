CREATE OR REPLACE
	FUNCTION FUNC_RETSALARIO(P_IDFUNC IN NUMBER)
		RETURN NUMBER
IS
  V_SALARY EMPLOYEES.SALARY%TYPE;
  QTDE NUMBER(1);
BEGIN
  SELECT COUNT(*) INTO QTDE 
		FROM EMPLOYEES 
			WHERE EMPLOYEE_ID = P_IDFUNC;
  
  IF QTDE = 1 THEN
    SELECT SALARY INTO V_SALARY FROM EMPLOYEES WHERE EMPLOYEE_ID = P_IDFUNC;
  ELSE
    V_SALARY :=0;
  END IF;
  
	RETURN V_SALARY;
END;

